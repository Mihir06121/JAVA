import java.io.*;

class DemoIfElse
{
  public static void main(String args[])
  {
    int  num = -5;
    if (num == 0) {
      System.out.println("It is Zero");
    } else if(num > 0){
      System.out.println("It is +ve");
    } // end of if-else      
    else {
      System.out.println("It is -ve");
    } // end of if-else
  }
  
}
